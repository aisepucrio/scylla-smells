# pessoa.py

class PessoaDetalhada:
    def __init__(self, nome, idade, endereco, telefone, email, cpf, rg, data_nascimento, altura, peso):
        self.nome = nome
        self.idade = idade
        self.endereco = endereco
        self.telefone = telefone
        self.email = email
        self.cpf = cpf
        self.rg = rg
        self.data_nascimento = data_nascimento
        self.altura = altura
        self.peso = peso

    def __init__(self, nome, raca):
        self.nome = nome
        self.raca = raca

    def latir(self):
        print(f"{self.nome} está latindo: Au au!")

    def comer(self):
        print(f"{self.nome} está comendo.")

    def brincar(self):
        print(f"{self.nome} está brincando.")

    def dormir(self):
        print(f"{self.nome} está dormindo.")


